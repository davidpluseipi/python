# Decimal representation
x = 10

# Binary representation
x = 0b10  # this would be 2 in decimal
print(x)  # this prints out the decimal representation

print(bin(x))  # bin takes in any number and returns its binary representation

# Hexidecimal representation
x = 0x12c  # this would be 300 in decimal
print(x)
print(hex(x))

# Complex numbers, a + bi
x = 1 + 2j  # j can be lower or uppercase
print(x)
