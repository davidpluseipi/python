# Operators

# Addition
x = 10 + 3
print(x)

# Subtraction
x = 10 - 3
print(x)

# Multiplication
x = 10 * 3
print(x)

# Division
x = 10 / 3
print(x)

# Division without the remainder
x = 10 // 3
print(x)

# Modulus (Division but just the remainder)
x = 10 % 3
print(x)

# Exponent
x = 10 ** 3  # 10 to the power of 3, or "10 cubed"
print(x)
